nlados=int(input('Ingrese el numero de lados de la figura'))

if nlados==3:
    print('La figura es un Triagulo')
    
elif nlados==4:
    print('La figura es un Quadrilatero')
    
elif nlados==5:
    print('La figura es un Pentagono')
    
elif nlados==6:
    print('La figura es un Hexagono')
    
elif nlados==7:
    print('La figura es un Heptagono')
    
elif nlados==8:
    print('La figura es un Octagono')
    
elif nlados==9:
    print('La figura es un Nonagono')
    
elif nlados==10:
    print('La figura es un Decagono')
          
else:
    print('numero de lados incorrecto')
